#Largest and smallest of the three given number

#user input
num1 = float ( input ("Enter first number : "));
num2 = float ( input ("Enter second number : "));
num3 = float ( input ("Enter third number : "));

#Arranging them in the list
numList = [num1, num2, num3];

#Recognising largest and smallest number
print ("Largest number among the three numbers is = ", max(numList));
print ("Smallest number among the three numbers is = ", min(numList));

